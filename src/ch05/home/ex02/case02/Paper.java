package ch05.home.ex02.case02;

public class Paper {
	private String msg;
	private String WriterName;	// Name만 하면 Paper의 name임.
	
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public String getWriterName() {
		return WriterName;
	}
	public void setWriterName(String Name) {
		this.WriterName = Name;
	}
}